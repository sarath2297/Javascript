// basic array opperations
// push() - push item to the end

const weekdays = ['mon','tue','wed']
console.log(weekdays);
weekdays.push('thur')
console.log(weekdays);

// unshift() - push item to the start

weekdays.unshift('sun')
console.log(weekdays);

// pop() - to remove item from the end

weekdays.push(3)
console.log(weekdays);

weekdays.pop()
console.log(weekdays);

// shift() - to remove item from the start

weekdays.shift()
console.log(weekdays);